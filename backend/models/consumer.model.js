import mongoose from 'mongoose';

const consumerSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true,
        index: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        index: true
    },
    password: {
        type: String,
        required: true
    },
    contactNumber: {
        type: String,
        required: true
    },
    street: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    role: {
        type: String,
        enum: ['user', 'admin', 'outlet', 'superadmin'],
        default: 'user',
        index: true
    }
}, { timestamps: true });

const Consumer = mongoose.model('Consumer', consumerSchema);
export default Consumer;